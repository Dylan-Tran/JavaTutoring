package Lessons;

public class InitializingVariables {
    public static void main(String[] args) {
        /*
        type variable_name = value;
        */

        // Boolean
        boolean bool = true;

        // Integers size from least to greatest
        char a = 1;
        short b = 1;
        int c = 1;
        long d = 1;

        // Decimals
        double e = 1.2;

        // String
        char f = 'a';
        String str = "hello world";

        // We can reassign values using the = operator
        c = 100;

        // Important lesson
        // Object have methods and member variables while primitives do not
        // We can tell the differences because of the highlighting and capitalization
    }
}
